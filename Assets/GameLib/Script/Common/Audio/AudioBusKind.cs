namespace Game.Audio
{
    // ================================================================
    // AudioBusKind - オーディオバス種別
    // ================================================================
    //
    // ## 概要
    //
    // オーディオの出力先バスを分類する。
    // 各バスは独立したボリューム設定と AudioSource プールを持つ。
    //
    // ## バス一覧
    //
    // - Bgm: 背景音楽。通常1つのみ再生、フェードで切り替え
    // - Sfx: 効果音。多重再生可能
    // - Voice: ボイス。キャラクターの声
    // - Ui: UI 効果音。ポーズ中も再生される想定
    //
    // ================================================================

    /// <summary>
    /// オーディオバスの種別。
    /// </summary>
    public enum AudioBusKind
    {
        Bgm = 0,
        Sfx = 1,
        Voice = 2,
        System = 3,
    }
}
